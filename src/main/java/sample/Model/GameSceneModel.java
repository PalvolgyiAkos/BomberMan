package sample.Model;

public class GameSceneModel
{
    private int width = 700;
    private int height= 700;

    public GameSceneModel()
    {
    }

    public int getWidth()
    {
        return width;
    }

    public void setWidth(int width)
    {
        this.width = width;
    }

    public int getHeight()
    {
        return height;
    }

    public void setHeight(int height)
    {
        this.height = height;
    }
}
