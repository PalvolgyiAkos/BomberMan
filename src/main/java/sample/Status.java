package sample;

/**
 * This is an enum class which is an indicator of the game's current state.
 */
public enum Status
{
    /**
     * This indicates that the game is currently on the Game Page.
     */
    GAMEPAGE,
    /**
     * This indicates that the game is currently on the Main Page.
     */
    MAINPAGE,
    /**
     * This indicates that the game is currently Paused.
     */
    PAUSE,
    /**
     * This indicates that the game is currently on the End Page.
     */
    END,
    /**
     * This indicates that the game is currently on the Naming Page.
     */
    NAMING,
    /**
     * This indicates that the game is currently on the Scoreboard Page.
     */
    SCOREBOARD
}
